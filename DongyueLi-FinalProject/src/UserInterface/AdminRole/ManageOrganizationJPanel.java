    private void populateCombo(){
        organizationJComboBox.removeAllItems();
        for (Organization.Type type : Organization.Type.values()){
            if(enterprise.getEnterpriseType().getValue().equals(Enterprise.EnterpriseType.Government.getValue())){
                if(type.equals(Organization.Type.HHS)){              
                    organizationJComboBox.addItem(type);                   
                }         
            }else if(enterprise.getEnterpriseType().getValue().equals(Enterprise.EnterpriseType.EquipmentManufacturer.getValue())){
                if(type.equals(Organization.Type.CustomerService)){              
                    organizationJComboBox.addItem(type);                  
                }         
            }else if(enterprise.getEnterpriseType().getValue().equals(Enterprise.EnterpriseType.Pharmacy.getValue())){
                if(type.equals(Organization.Type.DrugSales)){              
                    organizationJComboBox.addItem(type);                  
                }         
            }else{
                if(!type.getValue().equals(Organization.Type.Admin.getValue())&&!type.getValue().equals(Organization.Type.CustomerService.getValue())
                        &&!type.getValue().equals(Organization.Type.DrugSales.getValue())&&!type.getValue().equals(Organization.Type.HHS.getValue())){
                    organizationJComboBox.addItem(type);                   
                }
            }
        }              
    }
    
    private void populateTable(){
        DefaultTableModel model = (DefaultTableModel) organizationJTable.getModel();
        
        model.setRowCount(0);
        
        for (Organization organization : directory.getOrganizationList()){
            Object[] row = new Object[2];
            row[0] = organization.getOrganizationID();
            row[1] = organization.getName();
            
            model.addRow(row);
        }
    }
